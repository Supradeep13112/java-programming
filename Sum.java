import java.util.*;
public class Sum {
    public static void main(String[] args) {
        int a,b;
        Scanner s=new Scanner(System.in);
        System.out.print("Enter 1st number:");
        a=s.nextInt();
        System.out.print("Enter 2nd number");
        b=s.nextInt();
        System.out.println("Sum of "+a+" and "+b+" is "+(a+b));
    }
}
